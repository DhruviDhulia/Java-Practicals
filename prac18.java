import java.util.Scanner;

class Rectangle{
    int length;
    int breath;
    int a;
    int p;
    Rectangle(int l, int b){
length=l;
breath =b;
}
    int Area(int length,int breath){
        return a=length*breath;
    }
    int Perimeter(int length,int breath){
        return p=length+breath;
    }
}